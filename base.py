class BaseClass(object):

    def __init__(self, **kwargs):
        self.data = {}
        self.data.update(kwargs)

    ######################################################################
    def __getitem__(self, key):
        return self.data[key]

    ######################################################################
    def __setitem__(self, key, value):
        self.data[key] = value

    ######################################################################
    def __contains__(self, key):
        return key in self.data


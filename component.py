#!/opt/local/bin/python

import base


##########################################################################
##########################################################################
##########################################################################
class Component(base.BaseClass):

    def __init__(self, **kwargs):
        self.data = {
            "mass": 0,
            "space": 0,
            "power": 0,
        }
        self.data.update(kwargs)

    ######################################################################
    def __str__(self):
        return "<%s>" % self.__class__.__name__


##########################################################################
##########################################################################
##########################################################################
class Hull(Component):

    def __init__(self):
        Component.__init__(self)
        self["maxspace"] = 0


##########################################################################
##########################################################################
##########################################################################
class Misc(Component):

    def __init__(self):
        Component.__init__(self)


##########################################################################
##########################################################################
##########################################################################
class Engine(Component):

    def __init__(self):
        Component.__init__(self)
        self["force"] = 0

    ######################################################################
    def getForce(self):
        return self["force"]


##########################################################################
##########################################################################
##########################################################################
class PowerSupply(Component):

    def __init__(self):
        Component.__init__(self)
        self["power"] = 0

    ######################################################################
    def getPower(self):
        return self["power"]


##########################################################################
##########################################################################
##########################################################################
class Weapon(Component):

    def __init__(self):
        Component.__init__(self)
        self["range"] = 0
        self["damage"] = 0
        self["rate"] = 0
        self["accuracy"] = 0
        self["rate_cycle"] = 0

